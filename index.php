                  <div class="resume-content mr-auto">
                      <h3 class="mb-0">Inspiration</h3>
                      <div class="subheading mb-3"></div>
                      <p>Much of the setting itself comes from the eastern coast of the United States during its colonial period with other parts of the world being based on various nations that were active during that time period. The Empire that rules over the colonies is a combination of the Brittish Empire that ruled over the historic colonies as well as the Roman Empire. Other historic parellels include the hostility between The Empire and Lulon resembling that of England and France as well as Fileaxia resembling Egypt during its golden age.</p>
                      <p>In terms of literature, the two biggest pieces of inspiration for this project are <i>The Shadow Over Innsmouth</i> by H.P. Lovecraft and, to a lesser degree, <i>The Crucible</i> by Arthur Miller. Other works I've drawn from include <i>The Tell-Tale Heart</i> and <i>The Cask of Amontillado</i> by Edgar Allen Poe, the <i>Dark Sun</i> campaign setting from Dungeons and Dragons, and the wide world of <i>The Lord of the Rings</i> serries by J.R.R. Tolkien.</p>
