                  <div class="resume-content mr-auto">
                      <h3 class="mb-0">Concept</h3>
                      <div class="subheading mb-3"></div>
                      <p>Role playing games have always been a favorite of mine. I've always loved the ability to live in another world and make things your own, to chose the fate of everything around you. Yet, I've never found a game that could really satisfy my desire for a truely living world. A world where the choices of the players shape everything and the story is determined entirely by player actions. This is what this project sets out to accomplish.</p>
                      <p>Taking heavy inspiration from tabletop games like Dungeons and Dragons, and combining it with the multplayer scale of MMOs, this project aims to create a truely living world in which player choice drives the entire story. In effect, this game will be one massive tabletop RPG. Players will be able to log in and submit actions that they want their character to take and later check back in to see how their actions have shaped the world. Players should be able to see their actions develope the world and make an impact.</p>
                      <p>The hope is that players will be able to form a game world which they feel they have control over, a world in which anything feels possible.</p>
                  </div>
